#!/usr/bin/env node
/**
 * Template cleanliness guard.
 *
 * The product tree a recipient forks must contain ZERO personal/maintainer data,
 * so a fork ships clean with no intervention. This scans the shipped product
 * files for a denylist of personal identifiers and FAILS (exit 1) on any hit.
 *
 * Run: `bun run check:clean` (or `node scripts/check-template-clean.mjs`).
 * Wire into CI / pre-deploy so a regression can't ship.
 *
 * Scope = the product. ISA.md (the builder's design doc) is intentionally NOT
 * scanned here — it is not part of the running app; its handling is tracked
 * separately. Generated/vendored files are skipped.
 */
import { readdirSync, readFileSync, statSync } from "node:fs";
import { join, extname, relative } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = join(fileURLToPath(import.meta.url), "..", "..");

// Directories/files to scan (the shipped product). Everything else is ignored.
const INCLUDE_DIRS = ["app", "workers", "db", "public", "scripts"];
const INCLUDE_ROOT_FILES = ["README.md", "wrangler.jsonc", "package.json", "index.html"];

const SKIP_DIRS = new Set(["node_modules", ".git", "build", ".wrangler", "dist"]);
const SKIP_FILES = new Set([
  "bun.lock",
  "package-lock.json",
  "worker-configuration.d.ts", // generated by `wrangler types`
  "check-template-clean.mjs", // this file lists the patterns by design
]);
const TEXT_EXT = new Set([
  ".ts", ".tsx", ".js", ".jsx", ".mjs", ".cjs", ".json", ".jsonc",
  ".md", ".css", ".html", ".sql", ".txt", ".yaml", ".yml",
]);

// Denylist of PERSONAL/maintainer identifiers that must never ship in the product.
// Structural references the template legitimately needs are intentionally absent:
// the GitHub org `aitheroad-dev` (deploy-button repo URL) and the neutral
// placeholder `owner@example.com` are allowed.
const DENY = [
  { re: /yaron/i, label: "maintainer name" },
  { re: /hanoch/i, label: "maintainer name" },
  { re: /yaronkra/i, label: "maintainer gmail prefix" },
  { re: /aitheroad@/i, label: "maintainer email" },
  { re: /my-?jarvis/i, label: "source app name" },
  { re: /kloosterhof|leiderdorp/i, label: "maintainer address" },
  { re: /a28d6c975f2cf4e25fb2acb10bf4627e/i, label: "CF account id" },
  { re: /ee9fee75-f345/i, label: "live-fork D1 id" },
  { re: /\b99473836\b/, label: "KvK number" },
  { re: /NL671620472/i, label: "BTW number" },
];

function walk(dir, out) {
  for (const name of readdirSync(dir)) {
    if (SKIP_DIRS.has(name) || SKIP_FILES.has(name)) continue;
    const full = join(dir, name);
    const st = statSync(full);
    if (st.isDirectory()) walk(full, out);
    else if (TEXT_EXT.has(extname(name))) out.push(full);
  }
}

const files = [];
for (const d of INCLUDE_DIRS) {
  try {
    if (statSync(join(ROOT, d)).isDirectory()) walk(join(ROOT, d), files);
  } catch {
    /* dir may not exist yet */
  }
}
for (const f of INCLUDE_ROOT_FILES) {
  try {
    if (statSync(join(ROOT, f)).isFile() && !SKIP_FILES.has(f)) files.push(join(ROOT, f));
  } catch {
    /* file may not exist */
  }
}

const hits = [];
for (const file of files) {
  const lines = readFileSync(file, "utf8").split("\n");
  lines.forEach((line, i) => {
    for (const { re, label } of DENY) {
      if (re.test(line)) {
        hits.push(`${relative(ROOT, file)}:${i + 1}  [${label}]  ${line.trim().slice(0, 100)}`);
      }
    }
  });
}

if (hits.length > 0) {
  console.error(`✗ Template cleanliness check FAILED — ${hits.length} personal-data hit(s):\n`);
  for (const h of hits) console.error("  " + h);
  console.error(`\nClear these before shipping. (ISA.md is excluded by design.)`);
  process.exit(1);
}
console.log(`✓ Template clean — scanned ${files.length} product files, no personal data found.`);
